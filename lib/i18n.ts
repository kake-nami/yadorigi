export function translate(
  locale: Locale,
  key: string,
  vars?: Record<string, string | number>,
): string {
  let str = dict[locale][key] ?? dict['en'][key] ?? key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v))
    }
  }
  return str
}
